		<h2>Como Funciona</h2>
		<p>
			Bibliotroca é um sistema de trocas de livros entre usuários de forma simples e descomplicada. Cada livro seu enviado
			para outro usuário te dá direito a um crédito que pode ser usado para adquirir qualquer livro do nosso site.
		</p>
		<p>
			Saiba como realizar sua primeira troca com o nosso guia abaixo e comece agora mesmo a renovar sua biblioteca.
		</p>

		<br/>

		<h3>Começando</h3>
		<p>
			Quando você se cadastra no nosso site, automaticamente recebe 1 crédito para poder realizar a sua primeira troca.
		</p>
		<p>
			Você também já pode cadastrar todos aqueles seus livros que você não tem mais interesse para que fiquem disponíveis
			para outras pessoas que tenham interesse nele possam te enviar uma solicitação de troca.
		</p>

		<br/>

		<h3>Cadastrando seus livros</h3>
		<p>
			Para cadastrar seus livros no site e permitir que outras pessoas solicitem-o, vá para a página 'Estante' e clique
			no botão 'Cadastrar novo livro' à direita da tela. No formulário que irá aparecer vocẽ deve digitar algumas informações
			sobre o livro em questão. Ao clicar em 'Adicionar' seu livro estará visível para todos os usuários do Bibliotroca e pode
			ser solicitado a qualquer momento. Ele também ficará visível para você na página 'Minha Estante', de onde também pode
			ser editado.
		</p>

		<br/>

		<h3>Solicitando livros de outras pessoas</h3>
		<p>
			Para solicitar livros do seu interesse você primeiro precisa ter no mínimo 1 crédito disponível.
		</p>
		<p>
			Faça uma busca pelo título, autor ou editora do livro que deseja solicitar pelo campo de busca no canto superior direito
			e lhe mostraremos todos os títulos disponíveis no momento. Clicando no livro que gostaria solicitar uma nova janela ira
			se abrir com maiores informações sobre o livro e também do usuário que está disponibilizando-o. Caso deseje realizar
			uma solicitaço de troca, clique no link 'Solicitar livro', será pedido uma confirmação dos seus dados de entrega e pronto,
			agora é só aguardar o dono do livro aceitar sua solicitação e esperar seu livro chegar na sua casa.
		</p>
		<p>
			A partir do momento que a solicitação for enviada, 1 crédito seu será suspenso. Caso a solicitação se complete o usuário
			que lhe enviou o livro irá receber este seu crédito ao término da transação. Caso a solicitação não seja aceita pelo
			dono do livro, ou caso você cancele a solicitaçao, o crédito voltará para sua conta.
		</p>

		<br/>

		<h3>Gerenciando Solicitações</h3>
		<p>
			Todas as suas solicitações ficam agrupadas na aba 'Solicitações', separadas por 'Solicitações Recebidas' e 'Solicitações
			Enviadas' podendo ser consultadas a qualquer momento.
		</p>
		<p>
			Quando você receber uma solicitação de troca ela estará disponível para que você veja os dados da pessoa que a enviou,
			incluindo a localidade em que mora e sua reputação de trocas já feitas. Caso você aceite a solicitação, o endereço
			completo da pessoa solicitante irá ser mostrado para que você possa enviar o livro para o usuário. Assim que o solicitante
			receber seu livro e fizer a qualifacação de troca, vocẽ receberá 1 crédito na sua conta para poder realizar pedidos de livros.
		</p>
		<p>
			Da tela de Solicitações você também é capaz de visualizar as solicitações de troca que você realizou, podendo cancelar
			cada pedido a qualquer momento (desde que a solicitação ainda não tenha sido respondida).
		</p>

		<br/>

		<h3>Lista de Desejos</h3>
		<p>
			Na página 'Lista de Desejos' você pode cadastrar aqueles livros que pretende adquirir, mas por não possuir crédito no momento
			ou não tê-lo encontrado em nosso site ainda, você pode criar sua lista de livros para não esquecê-los mais tarde.
		</p>

		<br/>

		<h3>Em caso de dúvidas</h3>
		<p>
			Caso tenha ainda alguma dúvida a respeito do nosso site, entre em contato enviando um e-mail para contato@bibliotroca.com.br
		</p>

